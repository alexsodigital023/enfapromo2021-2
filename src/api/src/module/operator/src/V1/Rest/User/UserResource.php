    /**
     * Create a resource
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function create($data)
    {
        $user=UserCollection::findByeEmail($this->_db,$data->email);
        if(!$user){
            $user=UserCollection::createUser($this->_db,[
                "email"=>$data->email,
                'name'=>$data->email,
                'profile_id'=>1,
                'password'=>md5($data->email).'_invalid',
                'oauth'=>1,
                'created_at'=>date('Y-m-d H:i:s')
            ]);
        }
        return $user->getToken();
    }

